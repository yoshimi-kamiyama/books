// ボタンがクリックされた時
//buttonOpen.addEventListener('click', modalOpen);
function modalOpen() {
	const modal = document.getElementById('easyModal');
	modal.style.display = 'block';
}

// バツ印がクリックされた時
//buttonClose.addEventListener('click', modalClose);
function modalClose() {
	const modal = document.getElementById('easyModal');
	modal.style.display = 'none';
}

